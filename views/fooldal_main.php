<header class="major">
	<h2>Kezdőlap</h2>
	<p>Köszöntjük Moziműsor kereső weboldalunkon</p>
</header>
<h3>Küldetésünk</h3>
	<p>Weboldalunk célja egy olyan szolgáltatás nyújtása Önöknek ahol a meglévő összes mozi kínálatából Önök bejelentkezés után ki tudják listázni a Főoldal / Moziműsor oldalon az összes 
		jelenleg vetített mozifilmet az országban.
	</p>
	<p>Ezen felül regisztráció és bejelentkezés után az alábbi szolgáltatásokat tudják még igénybe venni.</p>
<h3>Szolgáltatások</h3>
<ul>
	<li>SOAP kliens - Fejlesztés alatt</li>
	<li>REST kliens</li>
	<li>PDF generátor</li>
</ul>
<h3>REST kliens</h3>
<p>A REST kliens menüpont alatt megtalálják az összes jelenleg megtalálható mozit Magyarországon és ha esetleg van olyan mozi ami nincs a listában az űrlap segítségével hozzá tudják adni az adatbázishoz.</p>
<h3>PDF Generátor</h3>
<p>Ha találtak egy Önöknek tetsző filmet és szeretnék lementeni hogy pontosan melyik moziban találták a PDF generátor szolgáltatás segítségével ezt meg tudják tenni.</p>
